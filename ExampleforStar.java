
public class ExampleforStar {
    public static void main(String[] args) {
        int x=1;
        while (x<6)
        {
         int y=0;
            while (y<x) 
            {
                System.out.print("*");
                y++;
        
            }
            System.out.println();
            x++;
        }
        
    }
}
